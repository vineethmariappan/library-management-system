    private void initTable(int year){
        try {
            DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
            tableModel.setRowCount(0);
            Connection conn = Dao.getConnection();
            Statement st = conn.createStatement();
            String sql = " SELECT COUNT(issue_list.issue_id) AS total,book.book_name AS book_name from issue_list JOIN book ON issue_list.book_id=book.book_id WHERE book.year='"+year+"' AND issue_list.issued=0 GROUP BY book.book_name";
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                int total = rs.getInt("total");
                String book = rs.getString("book_name");
                tableModel.addRow(new Object[]{book,total});
            }
            jTable1=new JTable(tableModel);

        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
