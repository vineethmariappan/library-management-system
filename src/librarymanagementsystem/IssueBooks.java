    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
        for (int count = 0; count < tableModel.getRowCount(); count++){
            try {
                Connection conn = Dao.getConnection();
                String book_name=tableModel.getValueAt(count, 0).toString();
                int book_id=findBookId(book_name);
                String sql="UPDATE issue_list SET issued=1 WHERE book_id="+book_id;
                System.out.println(sql);
                Statement st=conn.createStatement();
                st.executeUpdate(sql);
                
            } catch (SQLException ex) {
                Logger.getLogger(IssueBooks.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(IssueBooks.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        JOptionPane.showMessageDialog(null, "Books Issued Successfully ", "Book Management", JOptionPane.INFORMATION_MESSAGE);
        initTable(Integer.parseInt((String)jComboBox1.getSelectedItem()));
    }//GEN-LAST:event_jButton1MouseClicked

